// Introduction of let and const keyword

/*
    Before ES 6 the varible declaration was using keyword var
*/
var variable_name = 10;
/*
    The variables with var keyword are mutable
    duplicate declaration allowed hoisting
*/

/*
    ES 6 let
*/
let variable_name = 20;
/*
    let keyword is used as same as of var keyword
    these are mutable and are block scoped
*/

const variable_name = "Saaketh";
/*
    const keyword is used for constants or unchangable varibles
    these are immutable
*/
